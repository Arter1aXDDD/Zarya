CREATE TRIGGER trg_company_contact_updated_at
BEFORE UPDATE ON company_contact
FOR EACH ROW
EXECUTE FUNCTION set_updated_at();

DROP TRIGGER IF EXISTS trg_company_partner_updated_at ON company_partner;
CREATE TRIGGER trg_company_partner_updated_at
BEFORE UPDATE ON company_partner
FOR EACH ROW
EXECUTE FUNCTION set_updated_at();

DROP TRIGGER IF EXISTS trg_purchase_request_updated_at ON purchase_request;
CREATE TRIGGER trg_purchase_request_updated_at
BEFORE UPDATE ON purchase_request
FOR EACH ROW
EXECUTE FUNCTION set_updated_at();

DROP TRIGGER IF EXISTS trg_delivery_updated_at ON delivery;
CREATE TRIGGER trg_delivery_updated_at
BEFORE UPDATE ON delivery
FOR EACH ROW
EXECUTE FUNCTION set_updated_at();

